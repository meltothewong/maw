                <section>
                    <h1><span>Let's Talk Trash</span></h1>
                    <p>According to a 2012 report from the Environmental Protection Agency (EPA), Americans generate <a href="http://www.epa.gov/osw/nonhaz/municipal/pubs/2012_msw_fs.pdf" target="_blank">251 million tons of trash per year</a>. Our per capita trash disposal rate is a whopping 4.6 pounds per person, per day. Sixty-five percent come from our homes, while 35 percent come from commercial locations and institutions like restaurants, schools, hospitals and yes &mdash; businesses.</p>

                    <p>Where does it all end up? About 12% goes to incinerators, 34% gets recycled, and 54% gets buried in landfills.</p>

                    <p>If your trash goes to a landfill it is mummified in the ground. Trash in landfills doesn&#8217;t really decompose because it is packed so tightly in the ground that air and water can&#8217;t get in.</p> 

                    <p>Landfills have many adverse environmental impacts, including leaking toxic industrial and home-cleaning chemicals into our groundwater, and pumping methane into our atmosphere. Occasionally landfills catch on fire, releasing formaldehyde, hydrogen cyanide, hydrogen sulfide, nitrogen oxides and many other dangerous gasses <a href="http://cfpub.epa.gov/ncea/cfm/recordisplay.cfm?deid=158823" target="_blank">into the air we breathe</a>. 

                    <p>Recycling decreases our trash problem, but it&#8217;s not enough. We need to do more.</p>
                </section>
                <section>
                    <h1><span>Marketers Aren&#8217;t Helping</span></h1>
                    <p>As Marketers, we are sometimes under a strange social pressure to create a lot of &quot;stuff&quot; &mdash; t-shirts, pens, hats, bags, stress balls, and countless other tchotchkes with our logos on them (aka, promotional products, or &quot;swag&quot;).</p>
                    <p>We make these things because we hope that they will promote our brand and lead to customers in our pipelines. But do they actually help us achieve our goals? And even if they did, is the impact they have on our planet worth it?</p> 
                </section>
                <section>
                    <h1><span>Three dirty little secrets</span></h1>
                    <p>Here are three dirty little secrets that the tchotchke industry doesn&#8217;t want you to know:</p>
                    <ol><li><p>1. Roughly 2/3 of the marketing materials you produce end up in a hotel trash can before your target customer even leaves the conference.</p></li> 
                    <li><p>2. People will not, in fact, buy your product because you gave them a branded flashlight keychain.</p></li>
                    <li><p>3. People are more likely to remember <a href="http://psycnet.apa.org/journals/psp/104/2/199/" target="_blank">positive experiences</a> shared with other humans, rather than objects.</p></li>
                    <ol>
                </section>
                <section>
                    <h1><span>How We Can Change</span></h1>
                    <p>Most promotional products are made using cheap plastics or other non-biodegradable material that is inexpensive to produce. In addition &mdash; let&#8217;s be honest here &mdash; many of these items are really kind of unnecessary and not very useful. When we make a low-quality item that no one actually needs, it is more likely to get thrown away. Here&#8217;s what I propose:</p>
                    <ol><li><p>1. Let&#8217;s stop feeling like we &quot;have to&quot; make promotional items and think about alternatives &mdash; maybe we could offer consumable goods or services instead?</li></p>
                    <li><p>2. If we really need to make something, let&#8217;s look for products that can be recycled and or that are biodegradable.</li></p>
                    <li><p>3. Let&#8217;s try to only make high-quality items that people will actually use, and let&#8217;s make less of them.</li></p>
                    </ol>
